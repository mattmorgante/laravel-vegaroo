        <h2 class="subtitle">The Research</h2>

        <p class="content research">Heart disease, obesity, diabetes, cancer, strokes, hypertension, and cholesterol all have a common enemy: the vegan diet! The purpose of this page is to accumulate and distill the scientific research about nutrition and diet and present it in easily consumable chunks that real humans can understand.</p>

        <p class="content research">For example, often when reading scientific research I run across a sentence like this:</p>
        <ul>
        <li class="content research">Regarding cause-specific mortality, men and women had elevated risks for cancer mortality for red (HR, 1.22 [95% CI, 1.16-1.29], and HR, 1.20 [95% CI, 1.12-1.30], respectively) and processed meat (HR, 1.12 [95% CI, 1.06-1.19], and HR, 1.11 [95% CI 1.04-1.19], respectively) intakes. Furthermore, cardiovascular disease risk was elevated for men and women in the highest quintile of red (HR, 1.27 [95% CI, 1.20-1.35], and HR, 1.50 [95% CI, 1.37-1.65], respectively) and processed meat (HR, 1.09 [95% CI, 1.03-1.15], and HR, 1.38 [95% CI, 1.26-1.51], respectively) intakes. <a href="https://www.ncbi.nlm.nih.gov/pubmed/19307518">r</a> </li>
        </ul>

        <p class="content research">Although to most people this means nothing, it can be translated from scientist-speak to human. What it really means is:</p>

        <ul>
            <li>People who ate the most red meat were 20% more likely to die of cancer and at least 27% more likely to die of heart disease than those who ate the least. In women who consumed the most meat, the risk of dying from cardiovascular disease was 50% higher.</li>
        </ul>

        <h2 class="subtitle">Obesity, Heart Disease, Diabetes, & High Blood Pressure</h2>

        <p class="content research">The links between all of these diseases are too strong to separate, but the outcomes are impossible to ignore. A vegan diet protects against all of these deadly conditions.</p>

        <ul>
            <li class="content research">
                <a target="_blank" href="https://www.cambridge.org/core/journals/proceedings-of-the-nutrition-society/article/health-benefits-of-a-vegetarian-diet/8774207AE8B2CCB4A90D6ADDBC9EA89F">Key, Davey, Appleby. <i>Health benefits of a vegetarian diet:</i></a>
                Vegetarians have a lower mean BMI (by about 1 kg/m2), a lower mean plasma total cholesterol concentration (meaning they are less susceptible to diabetes), and a lower mortality from coronary artery disease (by about 25%).</li>

            <li class="content research">
                <a target="_blank" href="http://europepmc.org/abstract/med/19562864">Craig, Mangels, ADA. <i>Position of the American Dietetic Association: vegetarian diets.</i></a>

                Vegetarians have lower cholesterol, lower blood pressure, and lower rates of hypertension and type 2 diabetes than nonvegetarians. Furthermore, vegetarians tend to have a lower body mass index and lower overall cancer rates. </li>
            <li class="content research">
                <a target="_blank" href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC3419346/">Le, Sabate. <i>Beyond Meatless, the Health Effects of Vegan Diets: Findings from the Adventist Cohorts</i></a>

                Vegetarian diets confer protection against cardiovascular diseases, cardiometabolic risk factors, some cancers and total mortality. Compared to lacto-ovo-vegetarian diets, vegan diets offer additional protection for obesity, hypertension, type-2 diabetes, and cardiovascular mortality.</li>

            <li class="content research">
                <a target="_blank" href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC3419346/">Fraser. <i>Vegetarian diets: what do we know of their effects on common chronic diseases?</i></a>

                Vegetarians have lower rates of coronary heart disease, largely explained by low LDL cholesterol, probable lower rates of hypertension and diabetes mellitus, and lower prevalence of obesity. Overall, their cancer rates appear to be moderately lower than others living in the same communities, and life expectancy appears to be greater.</li>

            <li class="content research">
                <a target="_blank" href="https://www.ncbi.nlm.nih.gov/pubmed/21521229">Trumbo, Shimakawa. <i>Tolerable upper intake levels for trans fat, saturated fat, and cholesterol.</i></a>

                The three boosters of bad cholesterol (transfat, saturated fat, and dietary cholesterol) all stem from eating animal products and processed junk foods. </li>

            <li class="content research">
                <a target="_blank" href="http://ajcn.nutrition.org/content/89/5/1627S.full">Craig. <i>Health effects of vegan diets</i></a>

                Vegans tend to be thinner, have lower serum cholesterol, and lower blood pressure, reducing their risk of heart disease.</li>

            <li class="content research">
                <a target="_blank" href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC3419346/">Boening, Bechthold, Bub, et al. <i>Critical review: vegetables and fruit in the prevention of chronic diseases</i></a>

                Increasing the consumption of fruits and vegetables reduces the risk of hypertension, cornary artery disease, stroke, cancer, obesity, and type 2 diabetes.</li>


            <li class="content research">
                <a target="_blank" href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC2697260">Wang, Beydoun. <i>Meat consumption is associated with obesity and central obesity among US adults</i></a>

                Participants in the study who consumed high amounts of meat were 33 percent more likely to have central obesity.</li>


    <div class="article_container">
        <p class="content">Even for those with the strongest willpower, going vegan overnight is generally not recommended. To make switching away from a plant-based diet easier, commit to taking small actions that can be repeated everyday.</p>

        <p class="content">Over time, even small changes can create big differences in your well-being and environmental impact. Then once you feel comfortable and ready to take on the next step in the progression, add a new habit to your vegan lifestyle.</p>

        <p class="content">To get your imagination flowing, here are 7 simple swaps and actions you can take to start moving towards a plant-based diet today.</p>

        <ol>
            <li class="research">Milk to Almond Milk</li>

            <p class="content">Perhaps the easiest switch in terms of taste differences and general ease of use, grab a carton of Almond milk instead of Cow's milk next time you head to the grocery store. For most people the taste between the two types of milk is negligble, especially if it's an addition to a larger meal.</p>
            <li class="research">Beef to Chicken</li>

            <p class="content">There's nothing worse for both the <a href="/environmental-benefits">environment</a> and your
                <a href="/health-benefits-long-term">health</a> than eating red meat products like beef. Next time you're eating out or faced with the two options in a social situation, opt for chicken, fish or white meat.</p>
            <li class="research">Yogurt to Soy Yogurt or Coconut Yogurt</li>

            <p class="content">For many Vegetarians, yogurt is an important source of protein and calcium. But the <a
                        href="/stop-animal-cruelty">dairy industry</a> causes obscene levels of cruelty to cows and their young calves, making it all the easier to switch to plant-based dairy alternatives like soy or coconut yogurt. If yogurt is a common aspect of your diet, try one of these alternatives and see if the benefits for our planet and your health are worth the change in taste!</p>

            <li class="research">Cook A Vegetarian Dinner</li>

            <p class="content">For many people, even the word dinner conjures up a mental image of meat, potatoes, and a simple vegetable. Making a meal without meat, usually the main focus of the meal, is a difficult mental divide to cross. Yet vegetarian and vegan meals can be surprisingly hearty, rich, and flavorful. Try cooking a vegetarian dinner once a week to expand your repetoire. For inspiration, check out the
                <a href="/#curry">curry recipes on Vegaroo</a></p>

            <li class="research">Cheese to Vegan Cheeses</li>

            <p class="content">One of the most difficult changes for any aspiring vegan is the lack of cheese. A constant part of the Western diet, cheese is becoming increasingly common in a multitude of dishes. But vegans need not abstain from pizza and nachos forever!
                <a href="https://www.traderjoes.com/dietary-lists/vegan">Trader Joe's</a> now carries Vegan Mozzarella cheese alongside many other vegan foods. For those interested in creating their own cheesy vegan concoctions, there are plenty of
                <a href="http://amzn.to/2Ali7u3">cookbooks</a> designed to help with exactly that.</p>

            <li class="research">Processed Meats to Organic Whole Meats</li>
            <p class="content">If you are going to keep eating meat, make the switch from processed meats like Bacon and Ham to organic whole meats. Processed meats are directly linked to cancer, and are home to an absurd amount of hormones, colorings, and other unnatural supplements. Organic whole meats are healthier; the closer you can get your food products from the source, the better they are for you.</p>


            <li class="research">Eat Out At A Vegetarian Restuarant</li>

            <p class="content">It's increasingly true that vegetarians and vegans no longer need to fear the lack of options when eating out with family and friends. To delight your taste buds and inspire your vegan culinary options, try eating out at a vegetarian or vegan restaurant. This is a great way to expose yourself to new ingredients and flavors that you might not normally associate with a vegan diet.</p>
        </ol>


        <h2>TL;DR</h2>

        <p class="content">You don't have to go vegan overnight to start feeling better about your decisions and improving your health. Take small steps in the right direction by swapping out meat or dairy for an easy plant-based alternative or trying a few meals to see what you like and dislike!</p>
